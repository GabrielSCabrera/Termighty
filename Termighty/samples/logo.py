def step1(chars):
    grid = Grid.empty((ROWS, COLS))
    for i in range(ROWS):
        for j in range(COLS):
            if grid[i,j].char != chars[i][j]:
                grid[i,j].set_char(chars[i][j])
    return grid

def step2(args):
    grid = Grid.empty((ROWS, COLS))
    n = 0
    for i in range(ROWS):
        for j in range(COLS):
            grid[i,j].color_b.set_RGB(args[1][i][j])
            grid[i,j].set_char(args[0][n][i][j])
            n += 1
    return grid

